private void tampilData() {

    DefaultTableModel model = new DefaultTableModel();

    model.addColumn("ID");
    model.addColumn("No Kamar");
    model.addColumn("Tipe");
    model.addColumn("Nama Tamu");
    model.addColumn("Durasi");
    model.addColumn("Status");

    try {

        String sql = "SELECT * FROM reservasi";

        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {

            model.addRow(new Object[]{
                rs.getInt("id_reservasi"),
                rs.getString("nomor_kamar"),
                rs.getString("tipe_kamar"),
                rs.getString("nama_tamu"),
                rs.getInt("durasi_menginap"),
                rs.getString("status_reservasi")
            });
        }

        tblReservasi.setModel(model);

    } catch (Exception e) {
        System.out.println(e.getMessage());
    }
}

