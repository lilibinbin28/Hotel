    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    if (txtNomorKamar.getText().isEmpty()
        || txtNamaTamu.getText().isEmpty()
        || txtDurasi.getText().isEmpty()) {

    JOptionPane.showMessageDialog(this,
            "Data tidak boleh kosong");

    return;
}
        try {

        String sql =
        "INSERT INTO reservasi(nomor_kamar,tipe_kamar,nama_tamu,durasi_menginap,status_reservasi) VALUES(?,?,?,?,?)";

        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, txtNomorKamar.getText());
        pst.setString(2, cmbTipeKamar.getSelectedItem().toString());
        pst.setString(3, txtNamaTamu.getText());
        pst.setInt(4, Integer.parseInt(txtDurasi.getText()));
        pst.setString(5, cmbStatus.getSelectedItem().toString());

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Data berhasil disimpan");

        tampilData();
        bersihForm();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(this, e.getMessage());

    }
    }//GEN-LAST:event_btnSimpanActionPerformed

